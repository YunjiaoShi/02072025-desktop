def getBondDuration(y,face,couponrate,m,ppy=1):
  pvcfsum=0
  wsum=0
  cf=face*(couponrate/ppy)
  y_period = y / ppy
  m_periods = m * ppy
  for t in range(1,m_periods+1):
    pv=(1+y_period)**(-t)
    cashflow=cf if t<m_periods else (face+cf)
    pvcf=pv*cashflow
    pvcfsum=pvcfsum+pvcf
    w=pvcf*t
    wsum=wsum+w

  bondprice=pvcfsum

  bondDuration=wsum/bondprice

  return(bondDuration)

