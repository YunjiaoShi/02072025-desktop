def get_bond_price(y,face,couponrate,m):
  pvcfsum=0
  cf=face*couponrate
  for t in range(1,m+1):
    pv=(1+y)**(-t)
    pvcf=pv*cf
    pvcfsum=pvcfsum+pvcf

  bondprice=pvcfsum+face*pv
  return(bondprice)

